object LevelsObject {


  def parseLevel (s : String) : Seq[Seq[CellType]] = {
    def toGridType(c : Char) : CellType = c match {
      case ' ' => Empty()
      case '#' => Wall()
      case '@' => Player()
      case '$' => Box()
      case '.' => Target()
      case _ => Empty()
    }
    makeSquare((s : StringOps).linesIterator.toIndexedSeq.map(_.map(toGridType)))
  }

  def makeSquare(grid : Seq[Seq[CellType]]) : Seq[Seq[CellType]] = {
    // not all rows are necessarily equally long,
    // this pads the rows which are shorter
    val rowWidth = grid.map(_.length).max
    def pad(row : Seq[CellType]) = row ++ Seq.fill(rowWidth - row.length)(Empty())
    grid.map(pad)
  }

